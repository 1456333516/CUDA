#include <stdio.h>


/**
 * @brief 主函数，程序的入口点
 * 
 * 该函数是C++程序的起点，负责执行程序的主要逻辑。
 * 在此实现中，程序会向标准输出打印"Hello, World!"并换行，
 * 然后返回0表示程序正常结束。
 * 
 * @return int 返回值为0，表示程序成功执行
 */
int main(void)
{
	// 向标准输出流打印"Hello, World!"并换行
	printf("Hello, World!\n");
	
	// 返回0表示程序正常退出
	return 0;
}
